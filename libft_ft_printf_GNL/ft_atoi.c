#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			sign;
	long long	result;

	i = 0;
	sign = 1;
	result = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if ((result == LONG_MAX / 10 && (str[i] - '0') > LONG_MAX % 10)
			|| result > LONG_MAX / 10)
			return (-(sign == 1));
		result = str[i] - 48 + (result * 10);
		i++;
	}
	return (result * sign);
}
